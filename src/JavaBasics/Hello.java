package JavaBasics;
import java.lang.String;
public class Hello
{

public static void main(String[] args)
	{
		int i,j,n=10;
		String s1="hello";
		
		
		for(i=0;i<=n;i++)
		{
			for(j=0;j<=n;j++)
			{ 
				
		if(j < i)
				{
			
			System.out.print(s1.toCharAt[i]);
			
		}
		else
		{
			System.out.print(" ");
		}
	}

	System.out.println();
}
	}
}

